/**
 * Format report as plain text
 */
export function formatAsText(report: ExportReport): string {
  let text = `IBM BOB REPORT\n`;
  text += `${'='.repeat(80)}\n\n`;
  text += `Generated: ${new Date(report.generatedAt).toLocaleString()}\n\n`;

  if (report.documentInfo) {
    text += `DOCUMENT INFORMATION\n`;
    text += `${'-'.repeat(80)}\n`;
    text += `Name: ${report.documentInfo.name}\n`;
    text += `Filename: ${report.documentInfo.filename}\n`;
    text += `Pages: ${report.documentInfo.pageCount}\n`;
    text += `Uploaded: ${new Date(report.documentInfo.uploadedAt).toLocaleString()}\n`;
    text += `Benefits Found: ${report.documentInfo.benefitsCount}\n`;
    text += `Sections: ${report.documentInfo.sectionsCount}\n\n`;
  }

  if (report.summary) {
    text += `DOCUMENT SUMMARY\n`;
    text += `${'-'.repeat(80)}\n`;
    text += `${report.summary}\n\n`;
  }

  if (report.benefits && report.benefits.length > 0) {
    text += `EXTRACTED BENEFITS\n`;
    text += `${'-'.repeat(80)}\n\n`;
    report.benefits.forEach((benefit, index) => {
      text += `${index + 1}. ${benefit.title}\n`;
      text += `   Category: ${benefit.category}\n`;
      text += `   Value: ${benefit.exactValue}\n`;
      text += `   Description: ${benefit.description}\n`;
      if (benefit.conditions.length > 0) {
        text += `   Conditions:\n`;
        benefit.conditions.forEach(condition => {
          text += `     - ${condition}\n`;
        });
      }
      text += `   Reference: ${benefit.sectionRef} (Page ${benefit.pageNumber})\n`;
      text += `   Confidence: ${benefit.confidence}\n\n`;
    });
  }

  text += `CONVERSATION HISTORY\n`;
  text += `${'-'.repeat(80)}\n\n`;
  report.conversation.forEach((msg) => {
    const role = msg.role === 'user' ? 'USER' : 'ASSISTANT';
    text += `[${role}]${msg.timestamp ? ` - ${new Date(msg.timestamp).toLocaleString()}` : ''}\n`;
    text += `${msg.content}\n`;
    
    if (msg.citations && msg.citations.length > 0) {
      text += `\nCitations:\n`;
      msg.citations.forEach(citation => {
        text += `  - ${citation.sectionNumber} - ${citation.sectionTitle} (Page ${citation.pageNumber})\n`;
        text += `    "${citation.excerpt}"\n`;
      });
    }
    text += `\n${'-'.repeat(80)}\n\n`;
  });

  text += `Report generated by IBM Bob\n`;
  return text;
}

/**
 * Export report in specified format
 */
export function exportReport(
  messages: ChatMessage[],
  document: ProcessedDocument | null,
  options: ExportOptions
): string {
  const report = generateReport(messages, document, {
    ...options,
    includeMetadata: options.includeMetadata ?? true,
    includeBenefits: options.includeBenefits ?? true,
    includeCitations: options.includeCitations ?? true,
    includeTimestamps: options.includeTimestamps ?? true,
  });

  switch (options.format) {
    case 'json':
      return formatAsJSON(report);
    case 'markdown':
      return formatAsMarkdown(report);
    case 'html':
      return formatAsHTML(report);
    case 'txt':
      return formatAsText(report);
    default:
      return formatAsJSON(report);
  }
}

/**
 * Download report as file
 */
export function downloadReport(
  content: string,
  format: 'json' | 'markdown' | 'html' | 'txt',
  filename?: string
): void {
  const mimeTypes = {
    json: 'application/json',
    markdown: 'text/markdown',
    html: 'text/html',
    txt: 'text/plain',
  };

  const extensions = {
    json: 'json',
    markdown: 'md',
    html: 'html',
    txt: 'txt',
  };

  const timestamp = new Date().toISOString().replace(/[:.]/g, '-').slice(0, -5);
  const defaultFilename = `ibm-bob-report-${timestamp}.${extensions[format]}`;
  
  const blob = new Blob([content], { type: mimeTypes[format] });
  const url = URL.createObjectURL(blob);
  const link = document.createElement('a');
  link.href = url;
  link.download = filename || defaultFilename;
  document.body.appendChild(link);
  link.click();
  document.body.removeChild(link);
  URL.revokeObjectURL(url);
}

