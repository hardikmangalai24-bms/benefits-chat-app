/**
 * Export Report Utility
 * Generates comprehensive reports from chat conversations and document analysis
 */

import { ChatMessage, ProcessedDocument, ExtractedBenefit } from './types';

export interface ExportOptions {
  format: 'json' | 'markdown' | 'html' | 'txt';
  includeMetadata?: boolean;
  includeBenefits?: boolean;
  includeCitations?: boolean;
  includeTimestamps?: boolean;
}

export interface ExportReport {
  generatedAt: string;
  documentInfo?: {
    name: string;
    filename: string;
    pageCount: number;
    uploadedAt: string;
    benefitsCount: number;
    sectionsCount: number;
  };
  conversation: Array<{
    role: string;
    content: string;
    timestamp?: string;
    citations?: Array<{
      sectionNumber: string;
      sectionTitle: string;
      excerpt: string;
      pageNumber: number;
    }>;
  }>;
  benefits?: ExtractedBenefit[];
  summary?: string;
}

/**
 * Generate export report from chat messages and document
 */
export function generateReport(
  messages: ChatMessage[],
  document: ProcessedDocument | null,
  options: ExportOptions = { format: 'json' }
): ExportReport {
  const report: ExportReport = {
    generatedAt: new Date().toISOString(),
    conversation: messages
      .filter(msg => msg.id !== 'welcome') // Exclude welcome message
      .map(msg => ({
        role: msg.role,
        content: msg.content,
        ...(options.includeTimestamps && { timestamp: msg.timestamp.toISOString() }),
        ...(options.includeCitations && msg.citations && { citations: msg.citations }),
      })),
  };

  if (document && options.includeMetadata) {
    report.documentInfo = {
      name: document.name,
      filename: document.filename,
      pageCount: document.pageCount,
      uploadedAt: document.uploadedAt.toISOString(),
      benefitsCount: document.benefits?.length || 0,
      sectionsCount: document.sections?.length || 0,
    };
  }

  if (document && options.includeBenefits) {
    report.benefits = document.benefits;
  }

  if (document) {
    report.summary = document.summary;
  }

  return report;
}

/**
 * Format report as JSON
 */
export function formatAsJSON(report: ExportReport): string {
  return JSON.stringify(report, null, 2);
}

/**
 * Format report as Markdown
 */
export function formatAsMarkdown(report: ExportReport): string {
  let markdown = `# IBM Bob Report\n\n`;
  markdown += `**Generated:** ${new Date(report.generatedAt).toLocaleString()}\n\n`;

  if (report.documentInfo) {
    markdown += `## Document Information\n\n`;
    markdown += `- **Name:** ${report.documentInfo.name}\n`;
    markdown += `- **Filename:** ${report.documentInfo.filename}\n`;
    markdown += `- **Pages:** ${report.documentInfo.pageCount}\n`;
    markdown += `- **Uploaded:** ${new Date(report.documentInfo.uploadedAt).toLocaleString()}\n`;
    markdown += `- **Benefits Found:** ${report.documentInfo.benefitsCount}\n`;
    markdown += `- **Sections:** ${report.documentInfo.sectionsCount}\n\n`;
  }

  if (report.summary) {
    markdown += `## Document Summary\n\n${report.summary}\n\n`;
  }

  if (report.benefits && report.benefits.length > 0) {
    markdown += `## Extracted Benefits\n\n`;
    report.benefits.forEach((benefit, index) => {
      markdown += `### ${index + 1}. ${benefit.title}\n\n`;
      markdown += `- **Category:** ${benefit.category}\n`;
      markdown += `- **Value:** ${benefit.exactValue}\n`;
      markdown += `- **Description:** ${benefit.description}\n`;
      if (benefit.conditions.length > 0) {
        markdown += `- **Conditions:**\n`;
        benefit.conditions.forEach(condition => {
          markdown += `  - ${condition}\n`;
        });
      }
      markdown += `- **Reference:** ${benefit.sectionRef} (Page ${benefit.pageNumber})\n`;
      markdown += `- **Confidence:** ${benefit.confidence}\n\n`;
    });
  }

  markdown += `## Conversation History\n\n`;
  report.conversation.forEach((msg, index) => {
    const role = msg.role === 'user' ? '👤 User' : '🤖 Assistant';
    markdown += `### ${role}${msg.timestamp ? ` - ${new Date(msg.timestamp).toLocaleString()}` : ''}\n\n`;
    markdown += `${msg.content}\n\n`;
    
    if (msg.citations && msg.citations.length > 0) {
      markdown += `**Citations:**\n`;
      msg.citations.forEach(citation => {
        markdown += `- ${citation.sectionNumber} - ${citation.sectionTitle} (Page ${citation.pageNumber})\n`;
        markdown += `  > ${citation.excerpt}\n`;
      });
      markdown += `\n`;
    }
  });

  markdown += `\n---\n*Report generated by IBM Bob*\n`;
  return markdown;
}

